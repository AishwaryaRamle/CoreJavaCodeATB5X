package src.Basics_05;

public class Lab079 {
    public static void main(String[] args) {
        // While -> Init -> Condition -> Body -> Increment

        // Do While
        // Do While -> Init -> Body -> Condition -> Increment
        int a = 5;
        do {
            System.out.println(a);
            a--;
        } while (a > 0);

        int a1 = 0;
        do {
            System.out.println(a1);
            a1--;
        } while (a1 >-5);

    }
}
