package src.Basics_03_16Dec;

public class Lab010 {
    public static void main(String[] args) {
        int a=10;
        float b=45;
        // Compound Assignment Operators
        // +=, -=, /= ,*= , %=

        //compound + operator
        b+=30; //b=b+30
        System.out.println(b);

        //compound - operator
        int b1=90;
        b1-=89; //b1=b1-89
        System.out.println(b1);

        b1=b1+1;
        System.out.println(b1);

        //compound-Division operator
        b/=a; //(b=b/a i.e. 75/10)
        System.out.println(b);

        //unary operator
        int a1=-10;
        System.out.println(a1);
    }
}

